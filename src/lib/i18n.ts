// --- Store for reactive locale ---
type Listener = () => void;
let currentLocale: Locale = "ko";
const listeners = new Set<Listener>();

function getSnapshot(): Locale {
  return currentLocale;
}

function getServerSnapshot(): Locale {
  return "ko";
}

function subscribe(listener: Listener): () => void {
  listeners.add(listener);
  return () => listeners.delete(listener);
}

function setLocale(locale: Locale) {
  if (locale === currentLocale) return;
  currentLocale = locale;
  if (typeof window !== "undefined") {
    localStorage.setItem("locale", locale);
  }
  listeners.forEach((l) => l());
}

function detectLocale(): Locale {
  if (typeof window === "undefined") return "ko";
  const stored = localStorage.getItem("locale") as Locale | null;
  if (stored && translations[stored]) return stored;
  const browserLang = navigator.language.split("-")[0] as Locale;
  if (translations[browserLang]) return browserLang;
  return "ko";
}

function detectLocaleFromUrl(): Locale | null {
  if (typeof window === "undefined") return null;
  const segments = window.location.pathname.split("/");
  if (segments.length >= 2) {
    const urlLocale = segments[1] as Locale;
    if (translations[urlLocale]) return urlLocale;
  }
  // No locale prefix in URL means Korean (default locale)
  return "ko";
}

// Initialize on first client load
let initialized = false;
function initLocale() {
  if (initialized || typeof window === "undefined") return;
  initialized = true;
  // Prefer URL locale over stored/browser locale
  const urlLocale = detectLocaleFromUrl();
  currentLocale = urlLocale || detectLocale();
}

export function useLocale() {
  initLocale();
  const locale = useSyncExternalStore(subscribe, getSnapshot, getServerSnapshot);

  const changeLocale = useCallback((newLocale: Locale) => {
    setLocale(newLocale);
  }, []);

  const t = useCallback(
    (key: string): string => {
      return translations[locale]?.[key] ?? translations.ko[key] ?? key;
    },
    [locale]
  );

  return { locale, setLocale: changeLocale, t };
}
